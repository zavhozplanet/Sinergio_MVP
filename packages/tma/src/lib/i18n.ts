function detectLanguage(): string {
    try {
        const tg = (window as any).Telegram?.WebApp;
        const langCode = tg?.initDataUnsafe?.user?.language_code;
        if (langCode === 'ru') return 'ru';
        if (langCode === 'en') return 'en';
        return 'uk';
    } catch {
        return 'uk';
    }
}

